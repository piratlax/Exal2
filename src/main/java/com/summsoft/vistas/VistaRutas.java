    private void Tabla(String buscar) {
 // Definici√≥n de la cabecera y anchos de las columnas
        String[] cabecera = {"Id", "Nombre", "Origen", "Destino","Activa"};
        int[] anchos = {0, 50, 200, 200, 50};

// Obtener el modelo y limpiar la tabla
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.setRowCount(0);

// Agregar la cabecera al modelo
        model.setDataVector(null, cabecera);

// Establecer los anchos de las columnas
        for (int i = 0; i < cabecera.length; i++) {
            tabla.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            tabla.setFont(new java.awt.Font("Tahoma", 0, 14));
        }
        tabla.getColumnModel().getColumn(0).setMaxWidth(0);
        tabla.getColumnModel().getColumn(0).setMinWidth(0);
        tabla.getColumnModel().getColumn(0).setPreferredWidth(0);

// Rellenar la tabla con datos desde el DAO
        try {
            
            DaoRutas dao = new ImplRutas();
            List<MdlRutas> mdl = dao.lista(buscar);

            for (MdlRutas rut : mdl) {
                model.addRow(new Object[]{
                    rut.getId(),
                    rut.getNombre(),
                    rut.getOrigen(),
                    rut.getDestino(),
                    rut.getActiva()
                });
            }
        } catch (Exception e) {
            System.out.println("error " + e);
        }
    }

