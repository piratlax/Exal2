    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
       String Usuario=txtUsuario.getText();
       String Password=encryptPassword(txtPassword.getText());
       
       
       daoUsuario dao=new implUsuario();
        try {
            boolean acceso = dao.login(Usuario, Password);
           
            if (acceso){
                Principal principal = new Principal();
                principal.setVisible(true);
                this.dispose();
            }else{
              JOptionPane.showMessageDialog(null, "Usuario no reconocido", "Advertencia", JOptionPane.WARNING_MESSAGE);
              txtPassword.setText("");
            }
            
        } catch (Exception ex) {
            System.out.println("Error "+ex);
        }
    }//GEN-LAST:event_btnAccederActionPerformed

